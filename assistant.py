"""
assistant.py — версия на Mistral AI (бесплатный API, без карты).

МОДЕЛЬ ДАННЫХ: понятия "напоминание" как отдельной сущности больше нет.
Всё — задачи, различающиеся полем task_type:
  'event'   🕑 точная дата и время (приём, созвон, вылет)
  'marker'  🔔 точная дата, важно не забыть (день рождения, дедлайн)
  'regular' —  обычная задача, без точного времени

Логика прежняя: interpret_message() понимает и структурирует, не показывая
пользователю JSON; formulate_reply() формулирует обычный текст ответа.
"""

import json
import os
import time
from datetime import date

try:
    from mistralai import Mistral
except ImportError:
    from mistralai.client import Mistral

client = Mistral(api_key=os.environ.get("MISTRAL_API_KEY"))
MODEL = "mistral-small-latest"


def _call_with_retry(fn, max_attempts=4, base_delay=5):
    """Повторяет вызов при ошибке 429 (превышен лимит бесплатного тарифа)
    вместо того, чтобы сразу падать с ошибкой."""
    last_error = None
    for attempt in range(max_attempts):
        try:
            return fn()
        except Exception as e:
            if "429" in str(e) or "rate_limited" in str(e).lower():
                last_error = e
                time.sleep(base_delay * (attempt + 1))
            else:
                raise
    raise last_error


def _format_history(chat_history):
    if not chat_history:
        return "(история пуста, это начало общения)"
    lines = []
    for m in chat_history:
        speaker = "Пользователь" if m["role"] == "user" else "Ассистент"
        lines.append(f"{speaker}: {m['content']}")
    return "\n".join(lines)


def _format_open_tasks(open_tasks):
    if not open_tasks:
        return "(открытых задач нет)"
    lines = []
    for t in open_tasks:
        icon = {"event": "🕑", "marker": "🔔", "regular": ""}.get(t.get("task_type"), "")
        lines.append(
            f"id={t['id']} {icon} | {t['task']} | тип: {t.get('task_type')} "
            f"| дата: {t.get('due_date') or '—'} | время: {t.get('due_time') or '—'} "
            f"| приоритет: {t.get('priority')} | категория: {t.get('category') or '—'} "
            f"| статус: {t.get('status')}"
        )
    return "\n".join(lines)


def _format_lists(lists_with_items):
    if not lists_with_items:
        return "(списков пока нет)"
    lines = []
    for l in lists_with_items:
        items_str = ", ".join(l["items"]) if l["items"] else "(пусто)"
        lines.append(f"«{l['name']}»: {items_str}")
    return "\n".join(lines)


