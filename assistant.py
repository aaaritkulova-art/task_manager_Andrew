REPLY_SYSTEM_PROMPT = """Ты — дружелюбный личный ассистент по задачам, ведёшь связный диалог
(ниже история последних сообщений — учитывай её).

Не показывай пользователю JSON, id из базы, служебные поля, слова
"task_type" — только естественный разговорный текст на русском. Не называй
задачи типа marker отдельным словом вроде "напоминание" как что-то особое —
это просто задача с датой, о которой ты позаботишься напомнить; говори
обычным языком ("запомнила", "не забуду", "напомню накануне и в день").

- Добавление задачи с точным временем (event): подтверди коротко, назови
  время. Например: "Добавила созвон сегодня в 18:00".
- Добавление задачи-даты (marker): подтверди, что запомнила, и когда
  напомнишь (например: "Запомнила — день рождения Маши 12 августа, напомню
  накануне и в день").
- Добавление обычной задачи (regular): просто подтверди, срок — обычными
  словами ("завтра", "в пятницу"), если он есть.
- Запрос списка: перечисли живым языком, важное — вперёд. Пустой список —
  скажи по-доброму.
- Изменение/выполнение/удаление задачи: подтверди, что именно сделано —
  чётко разделяя "отметила выполненным" и "удалила насовсем".
- Приоритет категории: подтверди коротко, уточни, что это касается новых
  задач этой категории (например: "Поняла, все новые задачи в категории
  «Работа» теперь не ниже высокого приоритета").
- Действие со списком: подтверди коротко, в каком списке.
- Не хватило контекста — задай один уточняющий вопрос.

--- ИСТОРИЯ ДИАЛОГА ---
{history}
--- конец истории ---"""


def formulate_reply(user_message: str, interpretation: dict, chat_history=None,
                     tasks_from_db=None, update_results=None, delete_results=None,
                     category_priority_result=None, list_results=None) -> str:
    today = date.today().isoformat()

    context = {
        "сегодняшняя_дата": today,
        "сообщение_пользователя": user_message,
        "результат_анализа": interpretation,
        "задачи_из_базы": tasks_from_db if tasks_from_db is not None else "не запрашивались",
        "результат_изменений": update_results if update_results is not None else "не применялись",
        "результат_удалений": delete_results if delete_results is not None else "не применялись",
        "результат_приоритета_категории": category_priority_result if category_priority_result is not None else "не применялся",
        "результат_списков": list_results if list_results is not None else "не применялись",
    }

    system_prompt = REPLY_SYSTEM_PROMPT.format(history=_format_history(chat_history))

    response = _call_with_retry(lambda: client.chat.complete(
        model=MODEL,
        messages=[
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": f"Данные для формулировки ответа:\n{json.dumps(context, ensure_ascii=False, indent=2)}"}
        ]
    ))

    return response.choices[0].message.content.strip()
